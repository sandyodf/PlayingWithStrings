# String is sequence of characters
# it can be accessed using index
name = "Hello!!"
another_name = "Sandeep"

print("hello!!" + another_name)
print(name + another_name)

number = 101
print(str(number) + another_name)

# Accessing characters of a string
print(another_name[0])
print(another_name[6])

# Looping through the string
for chars in name:
    print(chars)

